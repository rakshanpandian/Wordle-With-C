//Helps get the word

void getword(FILE *fp, char *st, int n){
	//Takes the Uses the total lines function in random number generation. As the random number can be practically anything it is 
	//divided by modulous of the total number of lines +1 to count the last value to as num%n gives a range from 0 to (num-1)
	int tot=totallines(fp);
	int linenum=(rand()) % (tot+1);
	//Adds to account for a space at the end of the words and '\n'. n is the number of letters in the word.
	//Here it is 5 by default but if we want to add features we can more easily do for 6.
	int mult=n+2;
	//Multiplies the line number generated by the multiplier from the start of the file to find the word.
	fseek(fp,linenum*mult,SEEK_SET);
	char c;
	int count=0;
	for (c=fgetc(fp);c!='\n';c=fgetc(fp)){
		*(st+count)=c;
		count++;
	}
	*(st+n)='\0';
}

// Convert string to lowercase
void to_lowercase(char *str) {
	for (int i = 0; str[i]; i++) {
		str[i] = tolower((unsigned char) str[i]);
	}
}

// Validate input (only a-z or A-Z)
int is_valid_input(const char *input, int len) {
	for (int i = 0; i < len; i++) {
		if (!isalpha(input[i])) return 0;
	}
	return 1;
}

