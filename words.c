void game(int n, int *points, int practice){
	FILE *fp=fopen("five.txt","r");

	char st[n+2];
	getword(fp,st,n);
	int excess_user=n*2;
	char userword[excess_user];
	int count=0;
	int l;
	//They both keep taking guesses till the user gets the right answer.
	//Only in practice mode you get unlimitted guesses.
	if (practice){
		guess(userword,n);
		while (check(userword, st, n)!=n){
			printf("\nTry Again!\n");
			count++;
			guess(userword,n);
		}
		printf("\nYou got the answer right in %d tries!\n",count);

	}
	else {
		for (count=0;count<n;count++){
			guess(userword,n);
			if (check(userword, st, n)==n){
				printf("\nYou got the answer right!\n");
				l=count;
				break;
			}
		}
	}
	//Point system
	if (practice){
			printf("You won 10 points!\n"); 
			*points+=10; //Incorporate time to the points system
	}
	else{
		switch (l){
			case 0:printf("You won a 100 points!\n"); *points+=100; break;
			case 1:printf("You won 50 points!\n"); *points+=50; break;
			case 2:printf("You won 40 points!\n"); *points+=40; break;
			case 3:printf("You won 30 points!\n"); *points+=30; break;
			case 4:printf("You won 20 points!\n"); *points+=20; break;
			default:printf("Sorry but you lost! The word was supposed to be %s\n",st);
		}
	}
	fclose(fp);
}

