//Helps find the total number of lines by seeing how many times it has to run through \n. 
//It limits the total lines to 5000 as otherwise it kept on going for some reason. Besides that 5000 gives a good variety of words
//You can adjust the total number of words it can take.
int totallines(FILE *fp){
	int tot=0;
	int c=1;
	int i=0;
	//Makes sure that fp isn't null, that feof isn't reached, the third condition is additional and just an extra precaution. 
	//The forth limits the pool of words
	while (fp!=NULL && feof(fp)==0 && c!=EOF){
		//Takes it character by character
		for (c=getc(fp);(c!='\n') && (c!=EOF);c=getc(fp));
		tot+=1;
	}
	//Helps return the file back to the beginning
	rewind(fp);
	return tot;
}


